package com.kh.spring08.entity;

public class StudentDto {
	private String name;
	private int score;
	public StudentDto() {
		super();
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
}